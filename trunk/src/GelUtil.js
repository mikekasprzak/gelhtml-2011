function Util_IndexXY( _x, _y ) {
	return _x + (_y * this.width);
}
// - -------------------------------------------------------------------------------------------------------------- - //
function Util_GetXY( _x, _y ) {
	return this.Data[ _x + (_y * this.width) ];
}
// - -------------------------------------------------------------------------------------------------------------- - //
function Util_SetXY( _x, _y, _value ) {
	this.Data[ _x + (_y * this.width) ] = _value;
}
